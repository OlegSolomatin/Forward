                        </summary>
                        Смерть должника не влечет прекращение обязательства по заключенному им договору; наследник, принявший наследство, становится должником и несет ответственность по исполнению обязательства со дня открытия наследства. Согласно ГК РФ, временем открытия наследства признается день смерти гражданина. По наследству переходят имущественные обязанности, в том числе долги в пределах стоимости перешедшего к наследникам наследственного имущества.<br><br>
                        Предусмотрено два способа принятия наследства:<br><br>
                        1. Путем подачи по месту открытия наследства нотариусу или должностному лицу, уполномоченному в соответствии с законом выдавать свидетельства о праве на наследство, заявления о принятии наследства либо заявления наследника о выдаче свидетельства о праве на наследство.<br>
                        2. Совершением действий, свидетельствующих о фактическом принятии наследства: принятие мер по сохранению наследственного имущества, защита его от посягательств или притязаний третьих лиц; расходы за свой счет на содержание наследственного имущества; оплата за свой счет долгов наследователя или получение от третьих лиц причитавшихся наследователю денежных средств.<br>
                        В обоих случаях общий срок принятия наследства составляет шесть месяцев со дня его открытия.<br>
                        В отношениях с кредитором поручитель не вправе ссылаться на ограниченную ответственность наследников и требовать уменьшения размера своей обязанности по договору поручительства пропорционально стоимости наследственного имущества.
